
# numbers of list of tuples - RGBs
colors = [(255, 255, 255), (75, 0, 130), (0, 109, 176), (138, 43, 226), (65, 105, 225), (72, 61, 139)]

# with 2 'for loops'
colors_li = [ tuple([i/255 for i in rgb]) for rgb in colors ]

# with 1 'for loop'
colors_mp = [ list(map(lambda x: x/255, rgb)) for rgb in colors ]

# results
print(colors_li)
print(colors_mp)

